// 【题目链接】https://leetcode.cn/problems/lru-cache/
//
// 【对应引擎模块】
//   - 显存置换 (Eviction): 当 GPU 显存满时，淘汰最久未使用的 KV block
//   - vLLM Block Manager: 管理显存块的生命周期
//
// 【核心考点】
//   1. 双向链表 + 哈希表：O(1) 的 get/put 操作
//   2. 显存块生命周期管理：最近使用的块保持在链表头部
//   3. LRU 策略：当容量满时，删除链表尾部（最久未使用）的块
//
// 【实现要求】
//   - 实现 LRUCache 类：LRUCache(capacity), get(key), put(key, value)
//   - get 和 put 都要将访问的节点移到链表头部
//   - 使用哨兵节点简化边界条件处理
//   - 时间复杂度：O(1)，空间复杂度：O(capacity)
//
// 【Inference 映射】
//   - key: logical block id
//   - value: physical memory address
//   - capacity: GPU 显存能容纳的最大 block 数
//   - get/put: 访问/分配显存块时更新热度
//
// 【推荐语言】C++ (练习指针), Rust (练习 Rc<RefCell<>>)
