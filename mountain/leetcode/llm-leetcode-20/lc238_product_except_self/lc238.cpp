// 【对应引擎模块】
//   - Softmax: 归一化操作中的分母计算
//   - LayerNorm / RMSNorm: 归一化算子的统计量计算
//
// 【核心考点】
//   1. 前缀积 / 后缀积: 不使用除法计算除自身外的乘积
//   2. O(1) 额外空间: 利用输出数组存储中间结果
//   3. 两趟扫描: 第一趟计算前缀积，第二趟计算后缀积
//
// 【实现要求】
//   - 不能使用除法，要求 O(n) 时间，O(1) 空间（输出数组不算）
//   - 第一趟: result[i] = 前缀积（nums[0] * ... * nums[i-1]）
//   - 第二趟: result[i] *= 后缀积（nums[i+1] * ... * nums[n-1]）
//
// 【Inference 映射】
//   - 前缀/后缀积: 类似 Softmax 中先计算 sum，再归一化
