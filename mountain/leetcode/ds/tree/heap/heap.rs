/**
 * 堆 (Heap) - Rust 实现
 * 
 * ## 需求说明
 * 
 * 实现一个堆数据结构，要求：
 * 
 * ### 核心特性
 * - 完全二叉树
 * - 父节点值 >= 子节点值（大顶堆）或 <=（小顶堆）
 * - 通常用数组存储
 * 
 * ### 必须实现的操作
 * 1. `new(is_max)` - 创建新堆
 * 2. `push(value)` - 插入元素 O(log n)
 * 3. `pop()` - 提取并删除堆顶元素 O(log n)，返回 Option<T>
 * 4. `peek()` - 查看堆顶元素的引用但不删除 O(1)，返回 Option<&T>
 * 5. `heapify(index)` - 堆化操作
 * 6. `build_heap(vec)` - 从向量构建堆 O(n)
 * 7. `len()` - 返回堆大小
 * 8. `is_empty()` - 判断是否为空
 * 
 * ### 时间复杂度要求
 * - 插入/删除：O(log n)
 */
