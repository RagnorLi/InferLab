/**
 * 双端队列 (Deque) - Rust 实现
 * 
 * ## 需求说明
 * 
 * 实现一个双端队列数据结构，要求：
 * 
 * ### 核心特性
 * - 可以在队列的两端进行插入和删除操作
 * - 结合了栈和队列的特性
 * 
 * ### 必须实现的操作
 * 1. `new()` - 创建新双端队列
 * 2. `push_front(value)` - 在队首添加元素 O(1)
 * 3. `push_back(value)` - 在队尾添加元素 O(1)
 * 4. `pop_front()` - 删除并返回队首元素 O(1)，返回 Option<T>
 * 5. `pop_back()` - 删除并返回队尾元素 O(1)，返回 Option<T>
 * 6. `front()` - 返回队首元素的引用但不删除 O(1)，返回 Option<&T>
 * 7. `back()` - 返回队尾元素的引用但不删除 O(1)，返回 Option<&T>
 * 8. `is_empty()` - 判断队列是否为空 O(1)
 * 9. `len()` - 返回队列大小 O(1)
 * 
 * ### 实现方式
 * - 可以用双向链表实现
 * - 也可以用循环数组实现
 * 
 * ### 时间复杂度要求
 * - 两端插入：O(1)
 * - 两端删除：O(1)
 * - 查看两端：O(1)
 */
