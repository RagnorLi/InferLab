/**
 * 数组 (Array) - C++ 实现
 * 
 * ## 需求说明
 * 
 * 实现一个数组数据结构，要求：
 * 
 * ### 核心特性
 * - 连续内存存储，支持随机访问
 * - 通过索引访问元素，时间复杂度 O(1)
 * - 支持动态扩容
 * 
 * ### 必须实现的操作
 * 1. `Array(capacity)` - 构造函数，初始化数组，指定容量
 * 2. `~Array()` - 析构函数，释放内存
 * 3. `operator[](index)` - 通过索引访问元素 O(1)
 * 4. `append(value)` - 在数组末尾追加元素 O(1) 平均
 * 5. `insert(index, value)` - 在指定位置插入元素 O(n)
 * 6. `remove(index)` - 删除指定位置的元素 O(n)
 * 7. `length()` - 返回数组长度 O(1)
 * 8. `empty()` - 判断数组是否为空 O(1)
 * 
 * ### 可选优化
 * - 使用模板支持泛型
 * - 动态扩容策略（2倍扩容或1.5倍扩容）
 * - 缩容机制（当元素数量远小于容量时）
 * - 拷贝构造函数和赋值运算符
 * 
 * ### 时间复杂度要求
 * - 随机访问：O(1)
 * - 插入/删除：O(n)
 * - 追加：O(1) 平均（考虑扩容摊销）
 */
