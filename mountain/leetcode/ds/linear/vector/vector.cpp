/**
 * 动态数组 (Vector / Dynamic Array) - C++ 实现
 * 
 * ## 需求说明
 * 
 * 实现一个动态数组数据结构，要求：
 * 
 * ### 核心特性
 * - 基于数组实现，支持自动扩容
 * - 随机访问时间复杂度 O(1)
 * - 动态调整容量，避免频繁内存分配
 * 
 * ### 必须实现的操作
 * 1. `Vector(capacity)` - 构造函数，指定初始容量
 * 2. `~Vector()` - 析构函数
 * 3. `operator[](index)` - 通过索引访问 O(1)
 * 4. `append(value)` - 末尾追加 O(1) 平均
 * 5. `insert(index, value)` - 指定位置插入 O(n)
 * 6. `remove(index)` - 删除指定位置 O(n)
 * 7. `pop()` - 删除并返回末尾元素 O(1)
 * 8. `clear()` - 清空数组
 * 9. `capacity()` - 返回当前容量
 * 10. `reserve(size)` - 预留容量（可选）
 * 
 * ### 扩容策略
 * - 当 size >= capacity 时触发扩容
 * - 扩容倍数：建议 2 倍或 1.5 倍
 * 
 * ### 时间复杂度要求
 * - 随机访问：O(1)
 * - 末尾插入：O(1) 平均（摊销）
 * - 中间插入/删除：O(n)
 */
