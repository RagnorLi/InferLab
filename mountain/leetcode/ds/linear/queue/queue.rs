/**
 * 队列 (Queue) - Rust 实现
 * 
 * ## 需求说明
 * 
 * 实现一个队列数据结构，要求：
 * 
 * ### 核心特性
 * - FIFO（First In First Out）先进先出
 * - 从队尾入队，从队首出队
 * 
 * ### 必须实现的操作
 * 1. `new()` - 创建新队列
 * 2. `enqueue(value)` - 元素入队 O(1)
 * 3. `dequeue()` - 元素出队并返回 O(1)，返回 Option<T>
 * 4. `front()` - 返回队首元素的引用但不删除 O(1)，返回 Option<&T>
 * 5. `is_empty()` - 判断队列是否为空 O(1)
 * 6. `len()` - 返回队列大小 O(1)
 * 
 * ### 实现方式
 * - 可以用 Vec 或 VecDeque 实现
 * - 也可以用链表实现
 * 
 * ### 时间复杂度要求
 * - 入队：O(1)
 * - 出队：O(1)
 * - 查看队首：O(1)
 */
